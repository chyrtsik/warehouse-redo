    //============================== Helpers and UI event processing =================================
    private void initReportHeader() {
        fieldWarehouse.setText(warehouse.getName());
    }

    private void initComplectingTaskList() {
        ComplectingTaskFilter complectingTaskFilter = new ComplectingTaskFilter();
        complectingTaskFilter.setWarehouseId(warehouse.getId());
        complectingTaskFilter.setStates(new ComplectingTaskState[]{ComplectingTaskState.READY_FOR_SHIPPING});

        complectingTasksList = new ReadyForShippingComplectingTaskList(complectingTaskFilter, WareHouse.getUserSession().getUser());
        complectingTasksList.setReportMinor(getClass().getCanonicalName());

        complectingTasksListPanel.removeAll();
        complectingTasksListPanel.add((new TableReport(complectingTasksList, this)).getContentPanel(), GridLayoutUtils.getGrowingAndFillingCellConstraints());
    }

    private void initListeners() {
        changeWarehouse.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                onChangeWarehouse();
            }
        });
    }

    private void onChangeWarehouse() {
        //Changing destination warehouse.
        WarehouseFilter filter = new WarehouseFilter();
        filter.setComplectingUserId(WareHouse.getUserSession().getUser().getId());
        WarehouseSelectForm warehouseForm = new WarehouseSelectForm(filter);
        if (Dialogs.runProperties(warehouseForm)) {
            warehouse = warehouseForm.getWarehouse();
            ComplectingTaskWarehouseSaver.store(warehouse, getFrameId());
            fieldWarehouse.setText(warehouse.getName());

            //Changing warehouse in complecting tasks list.
            complectingTasksList.setWarehouse(warehouse);
            complectingTasksList.refreshData();
        }
    }

