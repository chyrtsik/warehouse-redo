package com.artigile.warehouse.adapter.spi.impl.excel;

/**
 * @author Valery Barysok, 6/20/11
 */

public class Dimension {

    private int top;
    private int left;
    private int bottom;
    private int right;

    public Dimension() {
    }

    public Dimension(int top, int left, int bottom, int right) {
        this.top = top;
        this.left = left;
        this.bottom = bottom;
        this.right = right;
    }

    public int getBottom() {
        return bottom;
    }

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }
}